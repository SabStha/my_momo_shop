let currentStep = 0;
let tourActive = false;

// Initialize tour
function initTour() {
    tourActive = true;
    currentStep = 0;
    showTourStep();
    document.getElementById('tour-overlay').classList.remove('hidden');
    document.body.style.overflow = 'hidden';
}

// Show current tour step
function showTourStep() {
    const step = tourSteps[currentStep];
    const mascot = document.getElementById('tour-mascot');
    const message = document.getElementById('tour-message');
    const highlight = document.getElementById('tour-highlight');
    
    // Update message
    message.textContent = step.message;
    
    // Update progress
    updateProgress();
    
    // Handle target element
    if (step.target) {
        const targetElement = document.querySelector(step.target);
        if (targetElement) {
            highlightElement(targetElement);
            positionMascot(targetElement, step.position);
        } else {
            // If target not found, position mascot in center
            positionMascot(null, 'center');
            highlight.style.display = 'none';
        }
    } else {
        // No target (welcome/completion steps)
        positionMascot(null, 'center');
        highlight.style.display = 'none';
    }
    
    // Update navigation buttons
    updateNavigationButtons();
}

// Highlight target element
function highlightElement(element) {
    const highlight = document.getElementById('tour-highlight');
    const rect = element.getBoundingClientRect();
    
    highlight.style.display = 'block';
    highlight.style.left = (rect.left - 12) + 'px';
    highlight.style.top = (rect.top - 12) + 'px';
    highlight.style.width = (rect.width + 24) + 'px';
    highlight.style.height = (rect.height + 24) + 'px';
    
    // Add smooth animation
    highlight.style.transition = 'all 0.3s ease-in-out';
    
    // Make element clickable and add glow effect
    element.style.zIndex = '60';
    element.style.position = 'relative';
    element.style.boxShadow = '0 0 20px rgba(110, 13, 37, 0.5)';
    element.style.borderRadius = '8px';
    
    // Add pulse animation to the element
    element.style.animation = 'pulse-glow 2s ease-in-out infinite';
}

// Position mascot relative to target
function positionMascot(element, position) {
    const mascot = document.getElementById('tour-mascot');
    const speech = document.getElementById('tour-speech');
    
    if (element && position !== 'center') {
        const rect = element.getBoundingClientRect();
        const windowWidth = window.innerWidth;
        const windowHeight = window.innerHeight;
        const mascotWidth = 120; // Approximate mascot width
        const mascotHeight = 120; // Approximate mascot height
        const speechWidth = 250; // Approximate speech bubble width
        const speechHeight = 100; // Approximate speech bubble height
        
        let left, top;
        
        switch (position) {
            case 'bottom-right':
                // Position mascot to the right and below the target
                left = Math.min(rect.right + 30, windowWidth - mascotWidth - 20);
                top = Math.min(rect.bottom + 30, windowHeight - mascotHeight - 20);
                break;
            case 'bottom-left':
                // Position mascot to the left and below the target
                left = Math.max(rect.left - speechWidth - 30, 20);
                top = Math.min(rect.bottom + 30, windowHeight - mascotHeight - 20);
                break;
            case 'top-right':
                // Position mascot to the right and above the target
                left = Math.min(rect.right + 30, windowWidth - mascotWidth - 20);
                top = Math.max(rect.top - speechHeight - 30, 20);
                break;
            case 'top-left':
                // Position mascot to the left and above the target
                left = Math.max(rect.left - speechWidth - 30, 20);
                top = Math.max(rect.top - speechHeight - 30, 20);
                break;
        }
        
        // Ensure mascot stays within viewport bounds
        left = Math.max(20, Math.min(left, windowWidth - mascotWidth - 20));
        top = Math.max(20, Math.min(top, windowHeight - mascotHeight - 20));
        
        mascot.style.left = left + 'px';
        mascot.style.top = top + 'px';
        mascot.style.right = 'auto';
        mascot.style.bottom = 'auto';
        mascot.style.transform = 'none';
        
        // Add smooth animation
        mascot.style.transition = 'all 0.5s ease-in-out';
    } else {
        // Center position for welcome/completion steps
        mascot.style.left = '50%';
        mascot.style.top = '50%';
        mascot.style.transform = 'translate(-50%, -50%)';
        mascot.style.right = 'auto';
        mascot.style.bottom = 'auto';
        mascot.style.transition = 'all 0.5s ease-in-out';
    }
}

// Update progress indicator
function updateProgress() {
    const progress = document.getElementById('tour-progress');
    progress.innerHTML = '';
    
    tourSteps.forEach((step, index) => {
        const dot = document.createElement('div');
        const isActive = index === currentStep;
        const isCompleted = index < currentStep;
        
        dot.className = `w-4 h-4 rounded-full transition-all duration-300 transform hover:scale-125 cursor-pointer ${
            isActive ? 'bg-[#6E0D25] shadow-lg scale-125' : 
            isCompleted ? 'bg-green-500' : 'bg-gray-300'
        }`;
        
        // Add click functionality to jump to specific step
        dot.addEventListener('click', () => {
            currentStep = index;
            showTourStep();
        });
        
        // Add tooltip
        dot.title = `Step ${index + 1}: ${step.id}`;
        
        progress.appendChild(dot);
    });
}

// Update navigation buttons
function updateNavigationButtons() {
    const prevBtn = document.getElementById('tour-prev');
    const nextBtn = document.getElementById('tour-next');
    
    prevBtn.disabled = currentStep === 0;
    nextBtn.textContent = currentStep === tourSteps.length - 1 ? 'Finish' : 'Next ‚Üí';
    
    prevBtn.style.opacity = currentStep === 0 ? '0.5' : '1';
}

// Navigation functions
function nextTourStep() {
    if (currentStep < tourSteps.length - 1) {
        currentStep++;
        showTourStep();
    } else {
        completeTour();
    }
}

function previousTourStep() {
    if (currentStep > 0) {
        currentStep--;
        showTourStep();
    }
}

function skipTour() {
    completeTour();
}

function completeTour() {
    tourActive = false;
    document.getElementById('tour-overlay').classList.add('hidden');
    document.getElementById('tour-highlight').style.display = 'none';
    document.body.style.overflow = '';
    
    // Show completion message
    showToast('Tour completed! You\'re ready to start ordering! üéâ', 'success');
    
    // Save tour completion to localStorage
    localStorage.setItem('amako-tour-completed', 'true');
}

// Toast notification function
function showToast(message, type = 'info') {
    const toast = document.createElement('div');
    toast.className = `fixed top-4 right-4 px-6 py-3 rounded-lg text-white ${
        type === 'success' ? 'bg-green-500' : type === 'error' ? 'bg-red-500' : 'bg-blue-500'
    } shadow-lg z-50 transform transition-all duration-300 ease-in-out`;
    toast.textContent = message;
    document.body.appendChild(toast);
    
    setTimeout(() => {
        toast.remove();
    }, 3000);
}

// Function to start tour manually
function startAmaKoTour() {
    initTour();
}

// Start tour when page loads
document.addEventListener('DOMContentLoaded', function() {
    // Check if tour should be shown
    const tourCompleted = localStorage.getItem('amako-tour-completed');
    const urlParams = new URLSearchParams(window.location.search);
    const showTour = urlParams.get('tour') === 'true';
    
    if (showTour || !tourCompleted) {
        setTimeout(() => {
            initTour();
        }, 1000);
    }
});

// Handle window resize
window.addEventListener('resize', function() {
    if (tourActive) {
        showTourStep();
    }
});
